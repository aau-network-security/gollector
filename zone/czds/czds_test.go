package czds

import (
	"errors"
	tst "github.com/aau-network-security/go-domains/testing"
	"github.com/aau-network-security/go-domains/zone"
	"github.com/rs/zerolog/log"
	"os"
	"testing"
)

func TestCzds(t *testing.T) {
	tst.SkipCI(t)

	user := os.Getenv("CZDS_USER")
	pass := os.Getenv("CZDS_PASS")
	cred := Credentials{
		Username: user,
		Password: pass,
	}
	auth := NewAuthenticator(cred)
	z := New(auth, "net")

	count := 0
	f := func(domain []byte) error {
		log.Debug().Msgf("%s", domain)
		count++
		return errors.New("error generated by test")
	}

	opts := zone.ProcessOpts{
		DomainFn:       f,
		StreamWrappers: []zone.StreamWrapper{zone.GzipWrapper},
		StreamHandler:  zone.ZoneFileHandler,
	}

	if err := zone.Process(z, opts); err != nil && count != 1 {
		t.Fatalf("error while processing zone: %s", err)
	}

}
